public class DefaultConstructor {
    String name = "Agus Ardiansyah Nh";
    String id = "L200214197";
    String address = "Solo";

    public DefaultConstructor(){
        System.out.println("Hello there," + "\n" +
                "I am " + this.name + "\n" +
                "My Id is " + this.id + "\n" +
                "And now I live in " + this.address);
    }

}





